"""
iupac_namer/ring_naming/polyspiro_vb.py

Von-Baeyer polyspiro nomenclature (P-24.2.2 / P-24.2.3 / P-24.2.4).

Produces names for spiro ring systems with THREE OR MORE rings whose
components are all monocyclic (alicyclic or heterocyclic):

    dispiro[3.2.3^{7}.2^{4}]dodecane
    trispiro[2.0.2^{4}.1.2^{8}.1^{3}]undecane
    6,7,13,14-tetraoxadispiro[4.2.4^{8}.2^{5}]tetradecane   (a-replacement)

The classic monospiro (two rings, one shared atom) case is handled in
``spiro.py`` (``spiro[a.b]alkane``).  This module handles the polyspiro case
where the legacy code previously emitted invalid NESTED spiro names such as
``spiro[cyclohexane-1,6'-spiro[5.5]undecane]`` (OPSIN-unparseable) or refused
to name at all.

Algorithm (P-24.2.2 / P-24.2.3)
-------------------------------
1. Identify spiro atoms = ring atoms shared by >= 2 of the SSSR rings.  In a
   pure spiro system each spiro atom has degree 4 and belongs to exactly two
   rings; removing it does not disconnect the rest within either ring.
2. Build the "arc multigraph": nodes are the spiro atoms; each edge is an arc
   = a maximal path of non-spiro ring atoms connecting two spiro atoms (or a
   spiro atom to itself for a terminal-ring loop, or a direct 0-atom bond
   between two adjacent spiro atoms).
3. The von-Baeyer spiro descriptor is generated by a closed Eulerian-style
   walk that traverses every arc exactly once, starting at a terminal ring,
   proceeding through each spiro atom, and returning to the first spiro atom.
   The walk visits arcs; the descriptor records each arc's linking-atom count,
   superscripting the locant of a spiro atom the second time it is reached.
4. Numbering follows the citation order of the descriptor.  We enumerate all
   valid Eulerian circuits (bounded by a complexity budget) and pick the
   lowest by:  (a) descriptor number set ascending,  (b) spiro-atom locant set
   ascending,  (c) descriptor sequence at first point of difference.
5. Heteroatoms are introduced by skeletal a-replacement on the finished
   carbon numbering, choosing the numbering that gives heteroatoms the lowest
   locant set as well (P-24.2.4.1.2).
"""

from __future__ import annotations

import logging
from typing import TYPE_CHECKING

from iupac_namer.data_loader import get_chain_stem, get_hw_tables, get_multiplier
from iupac_namer.types import Locant, NamedParent, Numbering

if TYPE_CHECKING:
    from iupac_namer.types import CandidateParent, RingSystem

logger = logging.getLogger(__name__)


# Heteroatom replacement priority (P-25.3.1).  Higher number = higher priority.
_REPLACEMENT_PRIORITY: dict[str, int] = {
    "O":  8, "S":  7, "Se": 6, "Te": 5, "N":  4,
    "P":  3, "As": 2, "Si": 1, "Ge": 0,
}

# Multiplier for the "spiro" count: 2 -> dispiro, 3 -> trispiro, ...
_SPIRO_MULT: dict[int, str] = {
    2: "dispiro", 3: "trispiro", 4: "tetraspiro", 5: "pentaspiro",
    6: "hexaspiro", 7: "heptaspiro", 8: "octaspiro", 9: "nonaspiro",
    10: "decaspiro",
}

# Complexity budget: skip systems with too many arcs to enumerate Eulerian
# circuits safely (keeps the engine responsive — see perf note in the brief).
_MAX_ARCS = 14


# ---------------------------------------------------------------------------
# Arc-multigraph construction
# ---------------------------------------------------------------------------

class _Arc:
    """An arc between two spiro atoms in the spiro arc-multigraph.

    ``a`` and ``b`` are the two endpoint spiro atom indices (a == b for a
    terminal-ring self-loop).  ``interior`` is the ordered tuple of non-spiro
    linking atoms along the arc (length 0 for a direct spiro-spiro bond).
    """

    __slots__ = ("a", "b", "interior", "aid")

    def __init__(self, a: int, b: int, interior: tuple[int, ...], aid: int):
        self.a = a
        self.b = b
        self.interior = interior
        self.aid = aid

    @property
    def size(self) -> int:
        return len(self.interior)

    def other(self, node: int) -> int:
        return self.b if node == self.a else self.a

    def interior_from(self, node: int) -> tuple[int, ...]:
        """Interior atoms ordered starting from the end adjacent to ``node``."""
        if node == self.a:
            return self.interior
        return tuple(reversed(self.interior))


def _find_spiro_atoms(rings: tuple[frozenset[int], ...]) -> set[int]:
    """Atoms belonging to >= 2 rings of the system (the spiro atoms)."""
    from collections import Counter
    c: Counter[int] = Counter()
    for r in rings:
        for atom in r:
            c[atom] += 1
    return {atom for atom, n in c.items() if n >= 2}


def _build_arc_graph(
    ring_system: "RingSystem", spiro_atoms: set[int], mol
) -> list[_Arc] | None:
    """Decompose the ring system into arcs between spiro atoms.

    Each ring is split at its spiro atom(s) into arcs.  Returns the list of
    arcs, or None if the topology is not a clean spiro system (e.g. a ring has
    no spiro atom, or fused/bridged sharing is present).
    """
    arcs: list[_Arc] = []
    aid = 0
    ring_set = set(ring_system.atom_indices)

    for ring in ring_system.rings:
        ring_spiros = [a for a in ring if a in spiro_atoms]
        if not ring_spiros:
            # A ring with no spiro atom cannot be part of a pure spiro system.
            return None
        # Walk the ring as a cycle; cut it at every spiro atom into arcs.
        cycle = _order_ring_cycle(ring, mol)
        if cycle is None:
            return None
        n = len(cycle)
        spiro_positions = [i for i, a in enumerate(cycle) if a in spiro_atoms]
        if len(spiro_positions) == 1:
            # Terminal ring: a single self-loop arc from the spiro atom around
            # the ring back to itself.
            sp = spiro_positions[0]
            interior = tuple(
                cycle[(sp + k) % n] for k in range(1, n)
            )
            arcs.append(_Arc(cycle[sp], cycle[sp], interior, aid))
            aid += 1
        else:
            # Ring contains >= 2 spiro atoms: split into consecutive arcs
            # between successive spiro positions around the cycle.
            sp_sorted = spiro_positions
            for k in range(len(sp_sorted)):
                start = sp_sorted[k]
                end = sp_sorted[(k + 1) % len(sp_sorted)]
                interior: list[int] = []
                j = (start + 1) % n
                while j != end:
                    interior.append(cycle[j])
                    j = (j + 1) % n
                arcs.append(
                    _Arc(cycle[start], cycle[end], tuple(interior), aid)
                )
                aid += 1
    return arcs


def _order_ring_cycle(ring: frozenset[int], mol) -> list[int] | None:
    """Return the atoms of ``ring`` in cyclic adjacency order."""
    atoms = set(ring)
    if not atoms:
        return None
    start = next(iter(atoms))
    cycle = [start]
    visited = {start}
    current = start
    while len(cycle) < len(atoms):
        moved = False
        for nb in mol.GetAtomWithIdx(current).GetNeighbors():
            j = nb.GetIdx()
            if j in atoms and j not in visited:
                cycle.append(j)
                visited.add(j)
                current = j
                moved = True
                break
        if not moved:
            return None
    # Verify the cycle closes (last atom adjacent to first).
    last = cycle[-1]
    first = cycle[0]
    if mol.GetBondBetweenAtoms(last, first) is None:
        return None
    return cycle


# ---------------------------------------------------------------------------
# Eulerian-circuit enumeration -> descriptor + numbering
# ---------------------------------------------------------------------------

class _Numbering:
    """A candidate von-Baeyer polyspiro numbering produced by one Eulerian
    traversal.  Holds the descriptor (with superscripts) and the atom->locant
    map needed to expose substituent placement to the engine.
    """

    __slots__ = ("descriptor", "spiro_locants", "atom_to_loc", "seq")

    def __init__(self, descriptor, spiro_locants, atom_to_loc, seq):
        self.descriptor = descriptor      # list[(size:int, superscript:int|None)]
        self.spiro_locants = spiro_locants  # sorted tuple of spiro-atom locants
        self.atom_to_loc = atom_to_loc    # dict[int,int]  full-mol idx -> locant
        self.seq = seq                    # tuple of segment sizes only (for tiebreak)

    def score(self) -> tuple:
        # P-24.2.2.1 / P-24.2.3.1 / .2:
        #   (1) descriptor number SET ascending (sorted segment sizes)
        #   (2) spiro-atom locant set ascending
        #   (3) descriptor SEQUENCE at first point of difference
        return (
            tuple(sorted(self.seq)),
            self.spiro_locants,
            self.seq,
        )


def _enumerate_numberings(
    arcs: list[_Arc], spiro_atoms: set[int]
) -> list[_Numbering]:
    """Enumerate von-Baeyer polyspiro numberings via Eulerian circuits.

    Each traversal must:
      - start with a terminal-ring arc (self-loop) whose linking-atom count is
        as small as possible (smallest terminal ring first, P-24.2.2),
      - cover every arc exactly once,
      - end back at the starting spiro atom (closed walk).
    """
    # adjacency: node -> list of arc indices incident on it
    incident: dict[int, list[int]] = {a: [] for a in spiro_atoms}
    for i, arc in enumerate(arcs):
        incident[arc.a].append(i)
        if arc.b != arc.a:
            incident[arc.b].append(i)
    total_arcs = len(arcs)

    # Candidate starting arcs: terminal self-loop arcs (a == b).  The smallest
    # such loop must come first per "starting at the smaller terminal ring".
    self_loops = [i for i, arc in enumerate(arcs) if arc.a == arc.b]
    if not self_loops:
        # No terminal ring as a self-loop — degenerate; bail (handled elsewhere).
        return []
    min_loop_size = min(arcs[i].size for i in self_loops)
    start_arcs = [i for i in self_loops if arcs[i].size == min_loop_size]

    results: list[_Numbering] = []
    # DFS over Eulerian circuits.  total_arcs <= _MAX_ARCS keeps this bounded.
    for start_aid in start_arcs:
        start_arc = arcs[start_aid]
        start_node = start_arc.a
        # The walk: list of (arc_index, traversed_from_node).
        used = [False] * total_arcs

        def dfs(current_node: int, path: list[tuple[int, int]]):
            if len(path) == total_arcs:
                if current_node == start_node:
                    nb = _build_from_path(path, arcs, spiro_atoms, start_node)
                    if nb is not None:
                        results.append(nb)
                return
            if len(results) > 4000:  # safety valve on pathological symmetry
                return
            for ai in incident[current_node]:
                if used[ai]:
                    continue
                arc = arcs[ai]
                nxt = arc.other(current_node)
                used[ai] = True
                path.append((ai, current_node))
                dfs(nxt, path)
                path.pop()
                used[ai] = False

        used[start_aid] = True
        dfs(start_node, [(start_aid, start_node)])
        used[start_aid] = False

    return results


def _build_from_path(
    path: list[tuple[int, int]],
    arcs: list[_Arc],
    spiro_atoms: set[int],
    start_node: int,
) -> "_Numbering | None":
    """Assign locants along an Eulerian path and build the descriptor.

    ``path`` is the ordered list of (arc_index, from_node).  We assign locants
    1.. to linking atoms and to spiro atoms the first time they are reached,
    superscripting the descriptor entry when a spiro atom is revisited.
    """
    atom_to_loc: dict[int, int] = {}
    spiro_loc: dict[int, int] = {}
    next_loc = 1
    descriptor: list[tuple[int, int | None]] = []
    seq: list[int] = []

    # The start node's locant is assigned the first time it is *reached as a
    # spiro atom*, which (per the examples) happens after the first terminal
    # arc's linking atoms are numbered.
    for step, (ai, from_node) in enumerate(path):
        arc = arcs[ai]
        interior = arc.interior_from(from_node)
        for atom in interior:
            atom_to_loc[atom] = next_loc
            next_loc += 1
        to_node = arc.other(from_node)
        # Reaching the spiro atom ``to_node``.
        if to_node in spiro_loc:
            # Revisited -> superscript its existing locant.
            descriptor.append((len(interior), spiro_loc[to_node]))
        else:
            spiro_loc[to_node] = next_loc
            atom_to_loc[to_node] = next_loc
            next_loc += 1
            descriptor.append((len(interior), None))
        seq.append(len(interior))

    # Sanity: every spiro atom must have a locant; total atoms consistent.
    if set(spiro_loc) != spiro_atoms:
        return None
    spiro_locants = tuple(sorted(spiro_loc.values()))
    return _Numbering(descriptor, spiro_locants, atom_to_loc, tuple(seq))


# ---------------------------------------------------------------------------
# Descriptor formatting + heteroatom a-replacement
# ---------------------------------------------------------------------------

def _format_descriptor(descriptor: list[tuple[int, int | None]]) -> str:
    """Render ``[2.0.2^{4}.1.2^{8}.1^{3}]`` from the segment list.

    Uses the ``^{N}`` superscript form, which OPSIN accepts (verified).
    """
    parts: list[str] = []
    for size, sup in descriptor:
        if sup is None:
            parts.append(str(size))
        else:
            parts.append(f"{size}^{{{sup}}}")
    return ".".join(parts)


def _build_replacement_prefix(
    heteroatoms, atom_to_loc: dict[int, int],
    lambda_map: "dict[int, int] | None" = None,
) -> str | None:
    """Build the a-replacement prefix (e.g. ``6,7,13,14-tetraoxa``).

    Returns "" when no heteroatoms, None when any heteroatom lacks an HW
    prefix (so the caller refuses rather than silently dropping it).

    ``lambda_map`` maps a heteroatom LOCANT to its non-standard valence
    (P-14.1.1 / P-31.1.4.3); that locant is then cited inline as
    "<loc>lambda<val>" (e.g. a P(VII) spiro centre at locant 5 →
    "5lambda7-phospha").
    """
    if not heteroatoms:
        return ""
    hw_tables = get_hw_tables()
    elem_to_prefix: dict[str, str] = {}
    for entry in hw_tables.get("prefixes", []):
        elem_to_prefix[entry["element"]] = entry["prefix"]

    items: list[tuple[int, str]] = []
    for hp in heteroatoms:
        loc = atom_to_loc.get(hp.atom_idx)
        if loc is None or hp.element not in elem_to_prefix:
            return None
        items.append((loc, hp.element))

    # Group by element, ordered by replacement priority (highest first).
    items.sort(key=lambda x: (-_REPLACEMENT_PRIORITY.get(x[1], -1), x[0]))
    elem_order: list[str] = []
    elem_to_locs: dict[str, list[int]] = {}
    for loc, elem in items:
        if elem not in elem_to_locs:
            elem_order.append(elem)
            elem_to_locs[elem] = []
        elem_to_locs[elem].append(loc)

    lmap = lambda_map or {}

    def _fmt_loc(loc: int) -> str:
        return f"{loc}lambda{lmap[loc]}" if loc in lmap else str(loc)

    segments: list[str] = []
    for elem in elem_order:
        locs = sorted(elem_to_locs[elem])
        pref = elem_to_prefix[elem]
        if len(locs) > 1:
            mult = get_multiplier(len(locs))
            if mult is None:
                return None
            pref = mult + pref
        loc_str = ",".join(_fmt_loc(x) for x in locs)
        segments.append(f"{loc_str}-{pref}")
    return "-".join(segments)


def _hetero_locant_set(heteroatoms, atom_to_loc: dict[int, int]) -> tuple:
    """Sorted tuple of heteroatom locants for the lowest-locant tiebreak,
    plus a priority-grouped sequence (P-24.2.4.1.2)."""
    locs: list[int] = []
    by_prio: dict[int, list[int]] = {}
    for hp in heteroatoms:
        loc = atom_to_loc.get(hp.atom_idx)
        if loc is None:
            return ((), ())
        locs.append(loc)
        prio = _REPLACEMENT_PRIORITY.get(hp.element, -1)
        by_prio.setdefault(prio, []).append(loc)
    grouped: list[int] = []
    for prio in sorted(by_prio, reverse=True):
        grouped.extend(sorted(by_prio[prio]))
    return (tuple(sorted(locs)), tuple(grouped))


# ---------------------------------------------------------------------------
# Public entry point
# ---------------------------------------------------------------------------

def name_polyspiro_vb(
    ring_system: "RingSystem",
    candidate: "CandidateParent",
    mol,
) -> list[NamedParent]:
    """Name a polyspiro (>= 3 ring) all-monocyclic system in von-Baeyer form.

    Returns a single-element NamedParent list on success, or [] when the
    topology is not a clean polyspiro of monocyclic rings (the caller then
    falls back to other spiro paths).
    """
    rings = ring_system.rings
    if len(rings) < 3:
        return []  # monospiro handled by spiro.py

    spiro_atoms = _find_spiro_atoms(rings)
    if len(spiro_atoms) < 2:
        return []

    # All spiro atoms must be neutral carbon for the all-carbon-skeleton
    # numbering (heteroatoms handled by a-replacement on ring positions, but
    # a heteroatom AT a spiro atom would need lambda/replacement that the
    # descriptor numbering does not modify — supported via replacement too).
    arcs = _build_arc_graph(ring_system, spiro_atoms, mol)
    if not arcs:
        return []
    if len(arcs) > _MAX_ARCS:
        logger.debug("polyspiro_vb: too many arcs (%d), skipping", len(arcs))
        return []

    total_atoms = ring_system.ring_size
    stem_base = get_chain_stem(total_atoms)
    if stem_base is None:
        return []

    numberings = _enumerate_numberings(arcs, spiro_atoms)
    if not numberings:
        return []

    heteroatoms = ring_system.heteroatoms or ()

    # Pick the best numbering.  Carbon-skeleton tiebreaks (descriptor) come
    # first (P-24.2.2/.3); among carbon-equivalent numberings, heteroatoms
    # break further ties (P-24.2.4.1.2).
    def full_score(nb: "_Numbering"):
        base = nb.score()
        if heteroatoms:
            return (base, _hetero_locant_set(heteroatoms, nb.atom_to_loc))
        return (base,)

    numberings.sort(key=full_score)
    best = numberings[0]

    # Lambda convention (P-14.1.1 / P-31.1.4.3): cite "<loc>lambda<val>" inline
    # for any skeletal heteroatom (including a hypervalent spiro centre, e.g.
    # P(VII)) whose actual valence differs from the element standard, so OPSIN
    # can reconstruct the exact valence.
    from iupac_namer.ring_naming.monocyclic import compute_lambda_value_map
    lambda_map = compute_lambda_value_map(
        [
            (best.atom_to_loc[hp.atom_idx], hp.element, hp.atom_idx)
            for hp in heteroatoms
            if hp.atom_idx in best.atom_to_loc
        ],
        mol,
    )

    # Build the replacement prefix from the chosen numbering.
    hetero_prefix = _build_replacement_prefix(
        heteroatoms, best.atom_to_loc, lambda_map=lambda_map
    )
    if hetero_prefix is None:
        return []  # refuse rather than drop heteroatoms

    spiro_count = len(spiro_atoms)
    spiro_word = _SPIRO_MULT.get(spiro_count)
    if spiro_word is None:
        return []
    descriptor_str = _format_descriptor(best.descriptor)

    # Endocyclic unsaturation (C=C / C#C within the spiro rings) -> -ene / -yne
    # suffix on the parent stem, with locants from the chosen numbering
    # (mirrors the monospiro path in spiro.py).
    from iupac_namer.ring_naming.monocyclic import (
        get_ring_bond_pairs,
        compute_ring_unsaturation_locants_from_numbering,
        _build_ring_unsaturation_suffix,
    )

    ring_bond_pairs = get_ring_bond_pairs(ring_system, mol)
    unsat_suffix = ""
    if ring_bond_pairs:
        atom_to_locant = {
            idx: Locant.numeric(loc) for idx, loc in best.atom_to_loc.items()
        }
        dbl, tri = compute_ring_unsaturation_locants_from_numbering(
            ring_bond_pairs, atom_to_locant
        )
        if dbl or tri:
            unsat_suffix = _build_ring_unsaturation_suffix(
                dbl, total_atoms, tri
            )

    if unsat_suffix:
        name_str = (
            f"{hetero_prefix}{spiro_word}[{descriptor_str}]"
            f"{stem_base}{unsat_suffix}"
        )
        stem = name_str[:-1] if name_str.endswith("e") else name_str
    else:
        name_str = f"{hetero_prefix}{spiro_word}[{descriptor_str}]{stem_base}ane"
        stem = f"{hetero_prefix}{spiro_word}[{descriptor_str}]{stem_base}an"

    # Expose the chosen numbering for substituent placement.
    assignments = tuple(
        (atom_idx, Locant.numeric(loc_val))
        for atom_idx, loc_val in sorted(best.atom_to_loc.items())
    )
    locant_set = tuple(Locant.numeric(i + 1) for i in range(total_atoms))
    numbering_options = (
        Numbering(_assignments=assignments, locant_set=locant_set),
    )

    return [NamedParent(
        candidate=candidate,
        name=name_str,
        stem=stem,
        alkyl_stem=None,
        naming_method="polyspiro_von_baeyer",
        indicated_hydrogen=None,
        numbering_options=numbering_options,
        ring_unsaturation_bonds=ring_bond_pairs if ring_bond_pairs else None,
    )]
